#include "3-calc.h"

/**
 * op_add - function adds two numbers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function subtract two numbers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function multiplies two numbers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function divides two numbers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function gets the mod of two numbers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
